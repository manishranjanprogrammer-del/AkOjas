package com.ojassoft.astrosage.varta.utils;

@SuppressWarnings("serial")
public class UIDataOperationException extends Exception {

    public UIDataOperationException() {
        super();
    }

    public UIDataOperationException(Throwable cause) {
        super(cause);

    }

    public UIDataOperationException(String message) {
        super(message);

    }

    public UIDataOperationException(String message, Throwable cause) {
        super(message, cause);

    }
}
