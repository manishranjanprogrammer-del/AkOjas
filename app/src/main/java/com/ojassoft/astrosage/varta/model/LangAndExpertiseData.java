package com.ojassoft.astrosage.varta.model;

public class LangAndExpertiseData {
    String langName;
    String langVal;
    boolean isLangSelected;

    public String getLangName() {
        return langName;
    }

    public void setLangName(String langName) {
        this.langName = langName;
    }

    public String getLangVal() {
        return langVal;
    }

    public void setLangVal(String langVal) {
        this.langVal = langVal;
    }

    public boolean isLangSelected() {
        return isLangSelected;
    }

    public void setLangSelected(boolean langSelected) {
        isLangSelected = langSelected;
    }
}
